# variavel nada mais é que uma referencia a um valor ou item, ou pode-se dizer que é um apelida a algum valor
nomeDaVariavel = "eu sou uma string, quando chamar nomeDaVariavel na verdade vai estar me chamando..."
# por exemplo, print(nomeDaVariavel) vai printar -> eu sou uma string, quando chamar nomeDaVariavel na verdade vai estar me chamando...
nomeDaVariavel = "pode ser mudado a qualquer momento"
# agora, print(nomeDaVariavel) vai printar -> pode ser mudado a qualquer momento
nomeDaVariavel = 1
# agora, print(nomeDaVariavel) vai printar -> 1